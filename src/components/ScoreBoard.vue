<template>
  <div>
    <b-container fluid>
      <b-row>
        <b-col>
          <b-modal
            id="scoreboard"
            :size="this.getScreenSize"
            :header-bg-variant="headerBgVariant"
            :header-text-variant="headerTextVariant"
            :body-bg-variant="bodyBgVariant"
            :body-text-variant="bodyTextVariant"
            :footer-bg-variant="footerBgVariant"
            :footer-text-variant="footerTextVariant"
            :visible="gameFinished"
          >
            <b-media-body>
              <b-container fluid>
                <b-row>
                  <b-col cols="2" id="totalCorrectWords">
                    Total
                    <hr />
                    {{totalScore}}/{{totalWords}}
                  </b-col>
                  <b-col cols="2" id="totalWords">
                    Accuracy
                    <hr />
                    {{(totalScore/totalWords)*100}}%
                  </b-col>
                  <b-col cols="6" offset="1" id="totalIncorrectWords">
                    Missed words
                    <hr />
                    <ol>
                      <li v-for="word in totalIncorrectWords" :key="word">{{word}}</li>
                    </ol>
                  </b-col>
                </b-row>
              </b-container>
            </b-media-body>
          </b-modal>
        </b-col>
      </b-row>
    </b-container>
  </div>
</template>

<script>
export default {
  data: function() {
    return {
      //styling
      headerBgVariant: "dark",
      headerTextVariant: "light",
      bodyBgVariant: "dark",
      bodyTextVariant: "light",
      footerBgVariant: "dark",
      footerTextVariant: "light",
      screenSize: { width: screen.width, height: screen.height }
    };
  },
  props: [
    "totalScore",
    "totalWords",
    "totalCorrectWords",
    "totalIncorrectWords",
    "gameFinished"
  ],
  computed: {
    getScreenSize: function() {
      var size = this.screenSize.width;
      var res = "";
      if (size <= 350) {
        res = "sm";
      } else if (350 < size && size <= 450) {
        res = "md";
      } else {
        res = "lg";
      }
      console.log(res);
      return res;
    }
  },
  methods: {
    //other
    fetchScreenSize: function() {
      this.screenSize = { width: screen.width, height: screen.height };
      console.log(this.screenSize.width);
    }
  },
  beforeMount: function() {
    this.fetchScreenSize();
  }
};
</script>

